def contar_palabras(cadena):
    palabras = cadena.split()
    frecuencia_palabras = {}
    for palabra in palabras:
        frecuencia_palabras[palabra] = frecuencia_palabras.get(palabra, 0) + 1
    return frecuencia_palabras

def palabra_mas_repetida(diccionario):
    palabra_max = max(diccionario, key=diccionario.get)
    frecuencia_max = diccionario[palabra_max]
    return (palabra_max, frecuencia_max)
def main():
    cadena = input("Ingrese una cadena de caracteres: ")
    frecuencia_palabras = contar_palabras(cadena)
    print("Diccionario de frecuencia de palabras:", frecuencia_palabras)

    palabra_max, frecuencia_max = palabra_mas_repetida(frecuencia_palabras)
    print("La palabra más repetida es '{}' con una frecuencia de {}.".format(palabra_max, frecuencia_max))
main()