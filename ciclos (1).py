def decimal_a_binario(decimal):
    if decimal == 0:
        return '0'

    binario = ''
    while decimal > 0:
        residuo = decimal % 2
        binario = str(residuo) + binario
        decimal //= 2

    return binario

def binario_a_decimal(binario):
    decimal = 0
    longitud = len(binario)
    for i in range(longitud):
        bit = int(binario[longitud - i - 1])
        decimal += bit * (2 ** i)

    return decimal
